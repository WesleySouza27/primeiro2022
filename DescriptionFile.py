a = input('digite algo ')
print(f'o tipo primitivo de dado é: {type(a)}')
print(f'é apenas letras? {a.isalpha()}')
print(f'é numero? {a.isnumeric()}')
print(f'Tem apenas letras maiusculas? {a.isupper()}')
print(f'tem apenas letras minusculas? {a.islower()}')
print(f'é um numero decimal? {a.isdecimal()}')
print(f'é um espaço vazio? {a.isspace()}')
print(f'é alphanumerico? {a.isalnum()}')
print(f'esta captalizada(começa com letra maiúscula? {a.istitle()}')

